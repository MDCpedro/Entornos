package torneo_padel;

public class Pareja {

    private Integer id; //Autogenerar al crear pareja
    private  String persona1;
    private  String persona2;
    private Integer telefono;

    // Constructor

    public Pareja(String persona1, String persona2, Integer telefono) {
        this.id = id;
        this.persona1 = persona1;
        this.persona2 = persona2;
        this.telefono = telefono;
    }

    public Integer getId() {
        return id;
    }

}
